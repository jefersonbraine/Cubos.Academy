
// - `+` - Soma
// - `-` - Subtração
// - `*` - Multiplicação
// - `/` - Divisão
// - `%` - Resto da Divisão inteira
// - `**` - Potencia(5 ao quadrado)


console.log(5 + 2) //⇒ 7

console.log(5 - 2) //⇒ 3

console.log(5 * 2) //⇒ 10

console.log(5 / 2) //⇒ 2.5

console.log(5 % 2) //⇒ 1

console.log(5 ** 2) // ⇒ 25


